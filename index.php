<h1>Introduction</h1>
<p>
Usually, ECOO contest problems are more difficult than in class problems for students. By practicing these questions, students are able to find out their level of ability to program different kinds of programs or web pages. By comparing sample answers with the student's answers, they can learn different ways to do the same program, or the answer can serve as a guide or those that has no idea of how to begin and solve the problem. With more practice, they are able to do questions more efficiently and effectively; this will significantly help them in the future when doing assignments, contests, or even working for an organization. However, ECOO contest problems can be hard to find, let alone the answers to those problems. 
</p>
<br>

<p>
Our project is a website that includes a collection of the past ECOO contest problems and answers. These include the board wide, the regional, and the final programming contests of ECOO. Users are able to sort and view past contest questions and solutions in an organized manner. They are able to make an account on the website which will enable them to express their opinions or ideas in the comment sections. The website is user friendly. It is easy to search for information on the website. 
</p>
<br>
<p>
Our website is not only useful for students, it is also very useful for teachers. They can coach students for ECOO contests using our website. They can assign students contest questions to do. Then, instead of having the teachers to solve the questions themselves, the teachers could just look at our answers and see if the students are correct. Therefore, we have decided to do this project because of its practical usage by many programming students and teachers. 
</p>
